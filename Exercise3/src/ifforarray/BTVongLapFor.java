package ifforarray;

public class BTVongLapFor {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }
        System.out.println("---------------------------");
        for (int j = 10; j >=0; j--) {
            System.out.println(j);
        }
        System.out.println("----------------------------");
        for (int i = 2; i < 100; i+=2) {
            System.out.println(i+ " .so chan");
        }
    }
}
